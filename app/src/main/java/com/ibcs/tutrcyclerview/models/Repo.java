package com.ibcs.tutrcyclerview.models;

public class Repo {

    private long id;
    private String name;

    public Repo() {
    }

    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
